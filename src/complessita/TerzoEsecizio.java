public class TerzoEsecizio
{
	public void metodo(int n)
	{
		int i=0, j;
		@SuppressWarnings("unused")
		double r = 1.0;
		for (; i < n; i++)
		{
			j = 1;
			while (j < i)
			{
				r += 2;
				j++;
			}// while
		}// for
	}// metodo
}
